def normalize_horei(name: str | None) -> str | None:
    """法令名を正式名称に正規化する（略称・部分一致を吸収。未知はそのまま返す）。"""
    if not name:
        return name
    s = str(name).strip().strip("　 ").rstrip("。")
    if s in OTHER_HOREI_LAWS:
        return s
    if s in HOREI_ALIASES:
        return HOREI_ALIASES[s]
    # 末尾に「法」が略されている等の部分一致（短い側が長い側に含まれる）
    for full in OTHER_HOREI_LAWS:
        if s and (s in full or full in s):
            return full
    return s

